
//=========“Java Literals”==========//
public class Example03 {
    public static void main(String[] args) {
        System.out.println(100);	//decimal
        System.out.println(0B1100100);	//binary
        System.out.println(0144);	//octal
        System.out.println(0X64);		//hexa-decimal
        System.out.println('a'); //char /character
        System.out.println('1');
        System.out.println('@');
        System.out.println('.');
        System.out.println("Hello\nJava");
        System.out.println("\\");
        System.out.println("\"");
        System.out.println("\\\\");
        System.out.println("\"C:\\Users\\Admin\\Desktop\\\"Calc.exe\"\"");
        System.out.println("===========");
        System.out.println(10 > 5);
        System.out.println(20 < 15);
        System.out.println(true);
        System.out.println(false);
        System.out.println("===========");
        System.out.println("1"); //String
        System.out.println(1);	//Integer
        System.out.println('1'); //Character
        System.out.println("Dusvi");
        System.out.println("1234");
        System.out.println("SD");

    }

}

